package com.company;

public class swapnum {
    public static void main(String[]args){
        int a = 1;
        int b = 3;
        System.out.println("value of a before swap a:"+a);
        System.out.println("value of a before swap b:"+b);
        int temp = 0;
        temp =a;
        a=b;
        b=temp;
        System.out.println("value of a after swap a:"+a);
        System.out.println("value of a before swap b: "+b);






    }
}
